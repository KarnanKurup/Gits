    private ArrayList<String> subids=new ArrayList<String>();
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String semid=jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        jComboBox2.removeAllItems();
        subids.clear();
        jComboBox2.addItem("-");
        if(!semid.equals("-")){
            try{
                ResultSet rs= new DbConnect().dbSelect("select subid,subname from subject where semid='"+semid+"'");
                while(rs.next()){
                    jComboBox2.addItem(rs.getString("subname"));
                    subids.add(rs.getString("subid"));
                }
            }catch(Exception e){
                MsgBox.errorBox(this, e);
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

