    private boolean Save(){
        String bno=jTextField1.getText();
        String bname=jTextField2.getText();
        String isbn=jTextField3.getText();
        String author=jTextField4.getText();
        
        if(bno.equals("")||bname.equals("")||isbn.equals("")||jComboBox2.getSelectedIndex()==0){
            MsgBox.errorBox(this, "Insufficient Data");
            return false;
        }else{
            String subid=subids.get(jComboBox2.getSelectedIndex()-1);
            try {
                new DbConnect().dbIdu("insert into books(bno,bname,isbn,bauthor,subid) values("+bno+",'"+bname+"','"+isbn+"','"+author+"',"+subid+")");
                return true;
            } catch (Exception ex) {
                MsgBox.errorBox(this, ex);
                return false;
            }
        }
    }

