    private void populate(){
        String q="select * from books where bno="+jTextField1.getText()+" and bname='"+jTextField2.getText()+"'";
        try {
            ResultSet rs=new DbConnect().dbSelect(q);
            if(rs.next()){
                String isbn=rs.getString("isbn");
                String author=rs.getString("bauthor");
                String subid=rs.getString("subid");
                String status=rs.getString("status");
                jTextField3.setText(isbn);
                jTextField4.setText(author);
                if(status.equals("R")){
                    jRadioButton1.setSelected(true);
                }else if(status.equals("I")){
                    jRadioButton2.setSelected(true);
                }else{
                    jRadioButton3.setSelected(true);
                }
                ResultSet r2=new DbConnect().dbSelect("select semid,subname from subject where subid='"+subid+"'");
                if(r2.next()){
                    String sem=r2.getString("semid");
                    jComboBox1.setSelectedItem(sem);
                    jComboBox2.setSelectedItem(r2.getString("subname"));
                }
            }
        } catch (Exception e) {
            MsgBox.errorBox(this, e);
        }
    }

    private void loadCombo(){
        try{
            DbConnect db= new DbConnect();
            ResultSet rs= db.dbSelect("select distinct(semid) from subject");
            while(rs.next()){
                jComboBox1.addItem(rs.getString(1));
            }
        }catch(Exception e){
            MsgBox.errorBox(this, e);
        }
        
    }
