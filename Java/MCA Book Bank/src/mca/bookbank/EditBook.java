    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String semid=jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        jComboBox2.removeAllItems();
        subids.clear();
        jComboBox2.addItem("-");
        if(!semid.equals("-")){
            try{
                ResultSet rs= new DbConnect().dbSelect("select subid,subname from subject where semid='"+semid+"'");
                while(rs.next()){
                    jComboBox2.addItem(rs.getString("subname"));
                    subids.add(rs.getString("subid"));
                }
            }catch(Exception e){
                MsgBox.errorBox(this, e);
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int op=JOptionPane.showConfirmDialog(this, "Are you sure you want to permanently delete this record", "Delete", JOptionPane.YES_NO_OPTION);
        if(op==JOptionPane.YES_OPTION){
            try {
                new DbConnect().dbIdu("delete from books where bno="+jTextField1.getText()+" and bname='"+jTextField2.getText()+"'");
                MsgBox.infoBox(this, "Record Deleted Successfully");
                this.dispose();
            } catch (Exception e) {
                MsgBox.errorBox(this, e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int op=JOptionPane.showConfirmDialog(this, "Are you sure you want to modify this Record?", "Update", JOptionPane.YES_NO_OPTION);
        if(op==JOptionPane.YES_OPTION){
            String bno=jTextField1.getText();
            String bname=jTextField2.getText();
            String isbn=jTextField3.getText();
            String author=jTextField4.getText();

            if(bno.equals("")||bname.equals("")||isbn.equals("")||jComboBox2.getSelectedIndex()==0){
                MsgBox.errorBox(this, "Insufficient Data");
            }else{
                String subid=subids.get(jComboBox2.getSelectedIndex()-1);
                String st="R";
                if(jRadioButton2.isSelected()){
                    st="I";
                }else if(jRadioButton3.isSelected()){
                    st="D";
                }
                try {
                    new DbConnect().dbIdu("update books set bno="+bno+",bname='"+bname+"',isbn='"+isbn+"',bauthor='"+author+"',subid="+subid+",status='"+st+"' where bno="+bookn+" and bname='"+bookna+"'");
                    MsgBox.infoBox(this, "Record Updated successfully");
                } catch (Exception ex) {
                    MsgBox.errorBox(this, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


